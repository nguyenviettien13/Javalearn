public class Example {
    public static void main(String[] args) {
        int songuyen1, songuyen2, thuong;
        songuyen1 = 21;
        songuyen2 = 2;
        thuong = songuyen1/ songuyen2;
        System.out.println("Thuong cua phep chia: "+ songuyen1 +" / " +
                songuyen2 + " = " + thuong);
        
        double sothuc1, sothuc2, dthuong;
        sothuc1 = 21;
        sothuc2 = 9;
        dthuong = sothuc1/ sothuc2;
        System.out.println("Thuong cua phep chia: "+ sothuc1 +" / " +
                sothuc2 + " = " + dthuong);
    }
}
